#include "libft.h"

void	ft_strrev(char *s)
{
	int	c;
	int	i;
	int	j;

	i = 0;
	j = ft_strlen(s);
	while (i < j)
	{
		c = s[i];
		s[i++] = s[j];
		s[j--] = c;
	}
}
